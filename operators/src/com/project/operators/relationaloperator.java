package com.project.operators;
//<,>,<=,>=,!
public class relationaloperator {
	public static void main(String[]args) {
		int a=100;
		int b=30;
		System.out.println((a<b));
		System.out.println((a>b));
		System.out.println((a>=b));
		System.out.println((a<=b));
		System.out.println("******************");
		System.out.println(!(a<b));
		System.out.println(!(a>b));
		System.out.println(!(a>=b));
		System.out.println(!(a<=b));
	}
}
