package com.project.operators;

public class logicaloperator {
    public static void main (String[] args) {
    	boolean a=true;
    	boolean b=false;
    	System.out.println("result of (a&&b) is :"+(a&&b));
    	System.out.println("result of (a||b) is :"+(a||b));
    	System.out.println("result of !(a&&b) is :"+!(a&&b));
    	System.out.println("result of !(a||b) is :"+!(a||b));
    }
}
